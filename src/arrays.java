public class arrays {
    public static void main(String[] args){
        /*
        Arrays são estruturas de dados que podem guardar mais de um dado
        na memória, diferente de uma variável comum.
        Todos eles começam no índice 0.
        */

        //array de numeros
        int[] nums = {5, 10, 15};

        //array de Strings
        String[] names = {"Vini", "Ana", "Paulo"};

        //Exibindo dados com array
        System.out.println(names[0] + " - " + nums[0]);


    }
}
